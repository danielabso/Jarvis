package br.com.Jarvis.domain;

public class Operadora {
	private Long idoperadora;
	private String nomeoperadora;
	private String coddearea;
	private String criadopor;
	private String criadoem;
	private String atualizadopor;
	private String atualizadoem;

	public Long getIdoperadora() {
		return idoperadora;
	}

	public void setIdoperadora(Long idoperadora) {
		this.idoperadora = idoperadora;
	}

	public String getNomeoperadora() {
		return nomeoperadora;
	}

	public void setNomeoperadora(String nomeoperadora) {
		this.nomeoperadora = nomeoperadora;
	}

	public String getCoddearea() {
		return coddearea;
	}

	public void setCoddearea(String coddearea) {
		this.coddearea = coddearea;
	}

	public String getCriadopor() {
		return criadopor;
	}

	public void setCriadopor(String criadopor) {
		this.criadopor = criadopor;
	}

	public String getCriadoem() {
		return criadoem;
	}

	public void setCriadoem(String criadoem) {
		this.criadoem = criadoem;
	}

	public String getAtualizadopor() {
		return atualizadopor;
	}

	public void setAtualizadopor(String atualizadopor) {
		this.atualizadopor = atualizadopor;
	}

	public String getAtualizadoem() {
		return atualizadoem;
	}

	public void setAtualizadoem(String atualizadoem) {
		this.atualizadoem = atualizadoem;
	}

}
